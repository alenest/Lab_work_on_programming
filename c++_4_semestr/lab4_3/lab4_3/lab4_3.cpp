#include <iostream>
#include <vector>
#include <locale> // Для setlocale

using namespace std; // Используем пространство имен std

// Функция для проверки упорядоченности элементов вектора
int проверитьУпорядоченность(const vector<int>& вектор) {
    // Используем итератор для прохода по вектору
    vector<int>::const_iterator итератор;
    for (итератор = вектор.begin(); итератор != вектор.end() - 1; ++итератор) {
        // Проверяем условие упорядоченности
        if (*итератор >= *(итератор + 1)) {
            // Возвращаем индекс первого элемента пары, нарушающей условие
            return distance(вектор.begin(), итератор); // Индексы считаются с 0
        }
    }
    // Если все элементы упорядочены, возвращаем -1
    return -1;
}

int main() {
    setlocale(LC_ALL, "Russian"); // Поддержка русского языка

    // Ввод массива с клавиатуры
    vector<int> числа;
    int размер;

    cout << "Введите количество элементов в массиве: ";
    cin >> размер;

    if (размер <= 0) {
        cout << "Массив пуст. Нет элементов для проверки." << endl;
        return 0;
    }

    cout << "Введите элементы массива через пробел: ";
    for (int i = 0; i < размер; ++i) {
        int элемент;
        cin >> элемент;
        числа.push_back(элемент); // Добавляем элемент в вектор
    }

    // Проверяем упорядоченность элементов
    int индекс = проверитьУпорядоченность(числа);

    // Выводим результат
    if (индекс != -1) {
        cout << "Найдена пара элементов, нарушающая упорядоченность на индексе: " << индекс << endl;
        cout << "Элементы: " << числа[индекс] << " и " << числа[индекс + 1] << endl;
    }
    else {
        cout << "Все элементы упорядочены по возрастанию." << endl;
    }

    return 0;
}